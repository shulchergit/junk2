#ifndef LOGGER_H
#define LOGGER_H

#include <stdarg.h>
#include <stdbool.h>
#include <stdint.h>
#include <stddef.h>

/* Log levels */
typedef enum {
    LOG_LEVEL_ERROR = 0,
    LOG_LEVEL_WARN = 1,
    LOG_LEVEL_INFO = 2,
    LOG_LEVEL_DEBUG = 3
} log_level_t;

/* Initialize logger */
void logger_init(bool debug_enabled);

/* Set debug mode */
void logger_set_debug(bool enabled);

/* Check if debug is enabled */
bool logger_is_debug_enabled(void);

/* Log functions */
void log_error(const char *fmt, ...);
void log_warn(const char *fmt, ...);
void log_info(const char *fmt, ...);
void log_debug(const char *fmt, ...);

/* Log with explicit level */
void log_message(log_level_t level, const char *fmt, ...);

/* Log binary data dump (debug only) */
void log_dump_bytes(const char *prefix, const uint8_t *data, size_t len);

#endif /* LOGGER_H */
